/**
 * Returns all species belonging to a given realm.
 * @param {string} realmId
 * @returns {Array}
 */
export function speciesByRealm(realmId) {
  return SPECIES.filter(s => s.realm === realmId);
}

/**
 * Returns a single species record by id, or undefined if not found.
 * @param {string} id
 * @returns {Object|undefined}
 */
export function speciesById(id) {
  return SPECIES.find(s => s.id === id);
}

